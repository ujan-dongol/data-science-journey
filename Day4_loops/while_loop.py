# break(Stop loop)    
for x in range(1,90):
    if x == 70 :
        break 
    print(x)
    
# continue(Skip Iteration)
for i in range(67):
    if i == 55:   
        # 55 will be escape or skips at by this statement 
        continue
    print(i)    